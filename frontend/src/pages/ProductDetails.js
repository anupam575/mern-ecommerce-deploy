import React, { useEffect, useState } from "react";
import { useParams } from "react-router-dom";
import { useDispatch, useSelector } from "react-redux";
import { toast } from "react-toastify";
import { v4 as uuidv4 } from "uuid";

import API from "../utils/axiosInstance";
import { addToCart } from "../redux/slices/cartSlice";
import { setProduct, clearProduct } from "../redux/slices/productSlice";
import ReviewSection from "./ReviewSection";

import "./style/ProductDetails.css";

function ProductDetails() {
  const { id } = useParams();
  const dispatch = useDispatch();

  const product = useSelector((state) => state.product.product);
  const [loading, setLoading] = useState(true);

  // ‚úÖ Product details fetch
  const fetchProductDetails = async () => {
    setLoading(true);
    try {
      const { data } = await API.get(`/api/v1/product/${id}`);
      dispatch(setProduct(data.product));
    } catch (error) {
      console.error("‚ùå Product fetch error:", error);
      toast.error(
        error.response?.data?.message || "‚ùå Failed to load product details"
      );
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    if (id) fetchProductDetails();
    return () => dispatch(clearProduct());
  }, [id, dispatch]);

  // ‚úÖ Add to cart
  const handleAddToCart = () => {
    if (!product) return;

    const cartItem = {
      id: product._id,
      title: product.name,
      price: product.price,
      img: product.images[0]?.url || "#",
      cartId: uuidv4(),
    };

    dispatch(addToCart(cartItem));
    toast.success("üõí Product added to cart!");
  };

  // ‚úÖ Loading UI
  if (loading)
    return (
      <div className="loading-placeholder">
        <p>Loading product details...</p>
      </div>
    );

  if (!product) return <p>‚ùå Product not found.</p>;

  return (
    <>
      <div className="amazon-product-container">
        {/* Product Images */}
        <div className="amazon-product-left">
          <img
            src={product.images[0]?.url || "#"}
            alt={product.name}
            className="amazon-product-image"
          />
        </div>

        {/* Product Info */}
        <div className="amazon-product-right">
          <h2 className="amazon-product-title">{product.name}</h2>
          <p className="amazon-product-description">{product.description}</p>
          <p className="amazon-product-rating">
            ‚≠ê {product.ratings.toFixed(1)} ({product.numOfReviews} reviews)
          </p>
          <p className="amazon-product-price">‚Çπ{product.price}</p>

          {/* ‚úÖ Category */}
          <p className="amazon-product-category">
            üì¶ Category: {product.category?.name || "Uncategorized"}
          </p>

          {/* ‚úÖ Stock status */}
          {product.inStock ? (
            product.lowStock ? (
              <p className="amazon-product-stock low-stock">
                ‚ö†Ô∏è Only {product.stock} left in stock ‚Äî order soon!
              </p>
            ) : (
              <p className="amazon-product-stock in-stock">‚úÖ In Stock</p>
            )
          ) : (
            <p className="amazon-product-stock out-of-stock">‚ùå Out of Stock</p>
          )}

          {/* ‚úÖ Actions */}
          <div className="amazon-product-actions">
            <button
              className="add-to-cart-btn"
              onClick={handleAddToCart}
              aria-label="Add product to cart"
              disabled={!product.inStock} // üö´ disable if not in stock
            >
              {product.inStock ? "Add to Cart" : "Unavailable"}
            </button>
          </div>
        </div>
      </div>

      {/* ‚úÖ Review Section */}
      <ReviewSection />
    </>
  );
}

export default ProductDetails;
